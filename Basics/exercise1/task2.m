%% 2.1 Evaluated answers are commented in down below

% a) 2 / 2 * 3 = 1 * 3 = 3, since we will calculate (2/2) * 3.
disp('Answer for 2.1. a)')
disp(2/2*3)

% b) 2 / 3^2 = 2/9 = 0.222..., since we will calculate the power operation first.
disp('Answer for 2.1. b)')
disp(2/3^2)

% c) (2 / 3)^2 = 2^2 / 3^2 = 4/9 = 0.444... since we raise the power first.
disp('Answer for 2.1. c)')
disp((2/3)^2)

% d) 2 + 3 * 4 - 4 = 2 + 12 - 4 = 10, multiply first
disp('Answer for 2.1. d)')
disp(2+3*4-4)

% e) 2^2 * 3 / 4 + 3 = 4 * 3 / 4 + 3 = 12/4 +´3 = 3 + 3 = 6
disp('Answer for 2.1. e)')
disp(2^2 * 3 / 4 + 3)

% f) 2^(2*3) / (4 + 3) = 2^6 / 7 = 64/7 = 9.1429..,
disp('Answer for 2.1. f)')
disp(2^(2*3) / (4 + 3))

% g) 2 * 3 + 4 = 6 + 4 = 10
disp('Answer for 2.1. g)')
disp(2 * 3 + 4)

% h) 2^3^2 = 8^2 = 64
disp('Answer for 2.1. h)')
disp(2^3^2)

% i) - 4^2 = -16
disp('Answer for 2.1. i)')
disp(- 4^2)

